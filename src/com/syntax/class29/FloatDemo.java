package com.syntax.class29;

public class FloatDemo {
    public static void main(String[] args) {

        float fl=3.5f;
        if(fl==3.5) {
            System.out.println("Equal");
        }else {
            System.out.println("not");
        }
        //equal

        fl=3.33f;
        if(fl==3.33) {
            System.out.println("Equal");
        }else {
            System.out.println("not");
        }
        //not equal

        System.out.println(10.0/3.0);
        //it goes on forever, but the computer must cut it off at some point because computer has limited memory.

    }
}
